package anylayout.util;

import java.awt.Dimension;

public class DimensionUtility
{
	public static Dimension multiply(final Dimension first,final int second)
	{
		return new Dimension(first.width*second,first.height*second);
	}

	public static Dimension divide(final Dimension first,final Dimension second)
	{
		return new Dimension(first.width/second.width,first.height/second.height);
	}
}
